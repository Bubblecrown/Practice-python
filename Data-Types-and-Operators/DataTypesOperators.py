# 1. คํานวณเปอร์เซ็นของจํานวนนักเรียนที่สอบผ่านจากจํานวนนักเรียนทั้งหมดในห้อง โดยเราจะกรอกข้อมูลจํานวนนักเรียนทั้ง 2 อย่าง มาให้โปรแกรมใช้ต่อ

# นักเรียนทั้งหมด (คน) : 40
# สอบผ่าน (คน) : 28
# สอบผ่าน 70 %

import math
std:int = int(input('นักเรียนทั้งหมด (คน) : '))
std_pass:int = int(input('สอบผ่าน (คน) : '))
print(f'สอบผ่าน {math.floor((100 * std_pass) / std)} %')
