# 8. ตรวจสอบอายุผู้ใช้งานว่าสามารถเข้าเล่นแอพของเราได้รึป่าว ผู้ใช้งานที่อายุต่ํากว่า 18 ปี จะเข้าเล่นแอพไม่ได้ โดยเราจะกรอกข้อมูลชื่อผู้ใช้งาน และอายุผู้ใช้งาน มาให้โปรแกรมใช้ต่อ

