# 15. ตรวจสอบว่าตัวเลข 2 ตัว เป็นเลขคู่ทั้งคู่ หรือเป็นเลขคี่ทั้งคู่ หรือไม่ โดยเราจะกรอกตัวเลขทั้ง 2 ตัว มาให้โปรแกรมใช้งานต่อ
# ตัวอย่างผลลัพธ์
# ตัวเลข 1 : 7
# ตัวเลข 2 : 8
# ผลการตรวจสอบ = False

num1 = int(input('ป้อนตัวเลข 1: '))
num2 = int(input('ป้อนตัวเลข 2: '))
result = (num1%2 == 0 and num2%2 == 0) or (num1%2 != 0 and num2%2 != 0)
print(f'ตัวเลข 1 : {num1}')
print(f'ตัวเลข 1 : {num2}')
print(f'ผลการตรวจสอบ = {result}')

# 16. แสดงผลการตรวจสอบว่าตัวเลขอยู่ระหว่าง 0 ถึง 100 หรือไม่ โดยเราจะกรอกตัวเลขมาให้โปรแกรมใช้งานต่อ
